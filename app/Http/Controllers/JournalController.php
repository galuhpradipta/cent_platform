        $journals = DB::table('journals as j')
                ->join('banks as b', 'j.bank_id', '=', 'b.id')
                ->select('j.amount', 'j.date', 'j.type', 'j.description', 'b.name as account_name')
                ->where('j.company_id', '=', $user->business_id)
                ->get();

        return view('journal.index', compact('journals'));
    }

